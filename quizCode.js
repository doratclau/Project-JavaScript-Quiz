    let score = Number(q1Value) + Number(q2Value) + Number(q3Value) + Number(q4Value) + Number(q5Value) + Number(q6Value) + Number(q7Value) + Number(q8Value) + Number(q9Value) + Number(q10Value);
    result = score;
    mark = result + " / 10"
    
